
import java.lang.*;

class incDec{
	public static void main(String args[]){
		
		int i=5;
		i++;
		System.out.println("Integer:"+i);
		float f=3.5f;
		f++;
		System.out.println("Float:"+f);
		char c='A';
		c++;
		System.out.println("Char:"+c);
		byte by=7;
		by++;
		System.out.println("Byte:"+by);
	}
}